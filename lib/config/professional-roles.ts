// 按行业获取角色列表
export function getRolesByIndustry(industry: keyof typeof INDUSTRIES): ProfessionalRole[] {
  return Object.values(PROFESSIONAL_ROLES).filter(role => role.industry === industry);
}

// 按分类获取角色列表
export function getRolesByCategory(category: string): ProfessionalRole[] {
  return Object.values(PROFESSIONAL_ROLES).filter(role => role.category === category);
}

// 获取所有角色ID列表
export function getAllRoleIds(): string[] {
  return Object.keys(PROFESSIONAL_ROLES);
}

// 获取角色详细信息
export function getRoleById(roleId: string): ProfessionalRole | null {
  return PROFESSIONAL_ROLES[roleId] || null;
}

// 搜索角色（按名称、描述、标签）
export function searchRoles(query: string): ProfessionalRole[] {
  const lowerQuery = query.toLowerCase();
  return Object.values(PROFESSIONAL_ROLES).filter(role => 
    role.name.toLowerCase().includes(lowerQuery) ||
    role.description.toLowerCase().includes(lowerQuery) ||
    role.tags.some(tag => tag.toLowerCase().includes(lowerQuery)) ||
    role.expertise.some(exp => exp.toLowerCase().includes(lowerQuery))
  );
}

// 获取角色统计信息
export function getRoleStatistics() {
  const totalRoles = Object.keys(PROFESSIONAL_ROLES).length;
  const industryCount = Object.keys(INDUSTRIES).length;
  
  const rolesByIndustry = Object.entries(INDUSTRIES).map(([key, info]) => ({
    industry: key,
    name: info.name,
    count: getRolesByIndustry(key as keyof typeof INDUSTRIES).length
  }));

  return {
    totalRoles,
    industryCount,
    rolesByIndustry,
    lastUpdated: new Date().toISOString()
  };
}